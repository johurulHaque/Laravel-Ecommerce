                  <b># Git repository</b><br>
                  Git repository is just a file location where you are storing all the files related to your project.<br><br>
                  Repository is a data structure used by VCS to stores the set of file as well as history of changes made to those file. <br><br>

                  <b>** git status</b><br>
                   displays the state of the working directory and the staging area(অবস্থা).
                   It lets you see which changes have been staged, which haven't, and which files aren't being tracked by Git. <br>
                   <u>Status output does not show you any information regarding the committed project history</u><br><br>

                   <b>** git commit</b><br>
                   The "commit" command is used to save your changes to the local repository. Every time you save it creates a unique ID (a.k.a. the "SHA" or "hash") that allows you to keep record of what changes were made when and by who.
